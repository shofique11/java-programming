public class Calculator {
        public int performAddition(int a, int b){
            return  a+b;
        }
        public double performAddition(double a, double b){
            return a + b;
        }
        public float performAddition(float a, float b){
            return a + b;
        }
        public int performAddition(int a, int b, int c){
            return a + b + c;
        }
}
